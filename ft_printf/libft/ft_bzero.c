#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*p;

	i = 0;
	p = (void *) s;
	while (i < n)
	{
		*p = '\0';
		p++;
		i++;
	}	
}
