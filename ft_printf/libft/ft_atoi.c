#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	numero;

	i = 0;
	sign = 1;
	numero = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == '\v')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = sign * (-1);
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		numero = numero * 10 + str[i] - '0';
		i++;
	}
	return (numero * sign);
}
